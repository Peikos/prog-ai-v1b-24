def rechthoek(h, b):
    """
    Bereken de oppervlakte van een rechthoek en teken een schets
    :param h: Hoogte vah het rechthoek
    :param b: Breedte van het rechthoek
    :return: Oppervlakte van het rechthoek
    """

    for i in range(h):
        print("#" * b)  # String * getal is de string herhalen

    return h*b


